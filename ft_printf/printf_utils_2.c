#include "printf.h"

int	find_spec(char *s)
{
	int i;

	i = 0;
	while ((ft_isalpha(s[i]) == 0) && s[i] != '%' && s[i] != '\0')
		i++;
	if (s[i] == '\0')
		return (-1);
	return (i);
}
